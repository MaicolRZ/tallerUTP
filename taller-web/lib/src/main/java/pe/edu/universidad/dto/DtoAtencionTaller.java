package pe.edu.universidad.dto;

public class DtoAtencionTaller {
	String dni_persona;
	String id_electro;
	String nombresCompleto;
	String marca;
	String tipo_electrodomestico;
	String modelo;
	String numero_serie;
	String descripcion;

	public String getDni_persona() {
		return dni_persona;
	}
	public void setDni_persona(String dni_persona) {
		this.dni_persona = dni_persona;
	}
	public String getId_electro() {
		return id_electro;
	}
	public void setId_electro(String id_electro) {
		this.id_electro = id_electro;
	}
	public String getNombresCompleto() {
		return nombresCompleto;
	}
	public void setNombresCompleto(String nombresCompleto) {
		this.nombresCompleto = nombresCompleto;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getTipo_electrodomestico() {
		return tipo_electrodomestico;
	}
	public void setTipo_electrodomestico(String tipo_electrodomestico) {
		this.tipo_electrodomestico = tipo_electrodomestico;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getNumero_serie() {
		return numero_serie;
	}
	public void setNumero_serie(String numero_serie) {
		this.numero_serie = numero_serie;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	

}
